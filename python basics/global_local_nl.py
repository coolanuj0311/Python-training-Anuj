'''Global,Local And Nonlocal variables


Global: Shared across the entire script.
Local: Confined to a specific function.
Nonlocal: Links a nested function to a variable in an enclosing function.

Favor local variables: Enhance code organization and clarity.
Use global variables judiciously: Only when truly necessary for sharing across multiple parts of your code.
Consider nonlocal variables for specific scenarios: But be mindful of potential readability issues.'''